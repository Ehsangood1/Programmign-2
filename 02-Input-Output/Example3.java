public class ConsoleOutputExample {
    public static void main(String[] args) {
        // Printing a message
        System.out.print("Hello, ");
        System.out.println("World!");

        // Printing formatted text
        int year = 2024;
        double pi = 3.14159;
        System.out.printf("The year is %d and the value of Pi is approximately %.2f%n", year, pi); // %d , this is a placeholder for an integer, %.2f%n this is placeholder for floating-point.
    }
}
